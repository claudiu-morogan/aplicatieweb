                                <h4>Mentenanta WEB</h4>
                                <p>
                                    Prin MENTENANTA SITE intelegem actiunea periodica de intretinere si actualizare a continutului unui site. Pentru ca Internet-ul este intr-o continua dezvoltare, iar regulile de clasare ale sit-urilor in topuri sunt in continua schimbare, orice site prezent pe Internet trebuie sa fie intretinut corespunzator.

                                    Sub aspectul optimizarii unui site pentru motoarele de cautare, serviciul de mentenanta / intretinere devine obligatoriu. Un motor de cautare (ex. Google, Yahoo, Bing, etc) care descopera ca la o indexare a unei pagini a exista modificari fata de indexarea anterioara va considera ca acea pagina se modifica mai des si va fi vizitata mai des in viitor. Insa daca nu sunt descoperite modificari de la o indexare la alta va considera acea pagina ca fiind stationara / statica si o va vizita mai rar, pagina putand pierde din acest motiv pozitii in afisarea rezultatelor cautarilor utilizatorilor pe respectivul motor de cautare.
                                    <h2>Avantaje:</h2>
                                    Un website cu continut de actualitate indexat corespunzator de motoare de cautare precum (Google, Yahoo, Bing, etc)</br>

                                    Externalizand acest serviciu veti reduce costurile lunare prin faptul ca nu aveti nevoie de un angajat care sa se ocupe de intretinerea site-ului dumneavoastra</br>

                                    Veti avea acces la o echipa de specialisti care se vor ocupa de nevoile site-ului dumneavoastra (lucru care este mai greu de obtinut de la un angajat)</br>

